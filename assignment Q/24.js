"use strict";
let MyName = "Omer khadim";
console.log(MyName === "Omer khadim"); //equality
console.log(MyName !== "Omer khadim"); //Inequality
let myname = "omer khadim";
console.log(myname.toLowerCase() === 'omer khadim'); //True
console.log(myname.toLowerCase() !== 'omer khadim'); //False
let mynumber = 980;
console.log(mynumber > 900); //Greater than
console.log(mynumber < 902); //Less than
console.log(mynumber >= 902); //Greater than or Equal to 
console.log(mynumber <= 900); //Less than or Equal to
console.log(mynumber = 980); //Equal to
console.log(mynumber <= 902 && mynumber > 902); // and operator
console.log(mynumber > 902 || mynumber < 902); // or operator
let myarray = [10, 30, 50, 70, 90];
console.log(myarray[3] === 80); //item not in a array index
console.log(myarray[4] === 90); //item in a array index
